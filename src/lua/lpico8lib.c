static int pico8_atan2(lua_State *l) {
    lua_Number x = lua_tonumber(l, 1);
    lua_Number y = lua_tonumber(l, 2);
    int32_t xbits = fix32_bits(x);
    int32_t ybits = fix32_bits(y);
    int32_t bits = 0x4000;
    if (xbits != 0) {
        /* Use integer abs instead of fix32_abs to emulate PICO-8’s behaviour
         * with e.g. atan2(0x8000, 0x8000.0001) */
        int64_t ax = (int64_t)xbits; if (ax < 0) ax = -ax;
        int64_t ay = (int64_t)ybits; if (ay < 0) ay = -ay;
        int64_t q = (ay << 16) / ax;
        if (q > 0x10000)
            bits -= atantable[((int64_t)1 << 32) / q >> 5];
        else
            bits = atantable[q >> 5];
    }
    if (xbits < 0) bits = 0x8000 - bits;
    if (ybits > 0) bits = -bits & 0xffff;
    /* Emulate a bug in PICO-8 with e.g. atan2(1, 0x8000) */
    if (xbits != 0 && ybits == (int32_t)0x80000000) bits = -bits & 0xffff;
    lua_pushnumber(l, fix32_from_bits(bits));
    return 1;
}

static int pico8_sqrt(lua_State *l) {
    int64_t root = 0, x = (int64_t)fix32_bits(lua_tonumber(l, 1)) << 16;
    if (x > 0) {
        /* Square root by abacus algorithm, Martin Guy @ UKC, June 1985. */
        int64_t a;
        for (a = (int64_t)1 << 46; a; a >>= 2, root >>= 1) {
            if (x >= a + root) {
                x -= a + root;
                root += a << 1;
            }
        }
    }
    lua_pushnumber(l, fix32_from_bits((int32_t)root));
    return 1;
}

